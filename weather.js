function weeklyTemperature(info) {
  for (var i = 0; i < info.daily.time.length; i++) {
    const weather = {
      dateDay: info.daily.time.at(i),
      minTemperature: info.daily.temperature_2m_min.at(i),
      maxTemperature: info.daily.temperature_2m_max.at(i),
      averageTemperature: function () {
        return (this.minTemperature + this.maxTemperature) / 2;
      },
      type: info.daily.weather_code[i],
      windSpeed: info.daily.wind_speed_10m_max.at(i),
    };

    weeklyForecast.push(weather);
  }
}

function averageWeeklyTemperature(array) {
  let sum = 0;

  array.forEach((temp) => (sum = sum + temp));

  return (sum / array.length).toFixed(2);
}

function createForecastTable(min, max) {
  let forecastSevenDays = `
  <h2>7 days forecast</h2>
  <table>
  <tbody>`;

  weeklyForecast.forEach(
    (day) =>
      (forecastSevenDays += `<tr>
        <td>${weatherCodeTranslation[day.type].icon}</td>
        <td>${day.minTemperature} / ${day.maxTemperature}</td>
        <td>${convertDate(day.dateDay)}</td>
      </tr>`)
  );

  forecastSevenDays += `
  </tbody>
  </table>
  <hr>
  <p>Average maximun temperature: ${max}</p>
  <p>Average minimun temperature: ${min}</p>`;

  return forecastSevenDays;
}

function convertDate(dateStr) {
  console.log(dateStr);
  const lang = "en-US";
  const date = new Date(`${dateStr} 00:00:00`);
  const dayNumber = date.getDate();
  const dayName = date.toLocaleDateString(lang, { weekday: "short" });
  const monthName = date.toLocaleString(lang, { month: "short" });

  return `${dayName} ${dayNumber} ${monthName}`;
}

function createDates(today) {
  var lessDay = 6;
  var moreDay = 1;
  var day;

  day = today.getTime() + moreDay * 24 * 60 * 60 * 1000;
  today.setTime(day); //Un dia mas de la fecha actual
  end = `${today.getFullYear()}-${today.getMonth() + 1}-${today.getDate()}`;
  day = today.getTime() - lessDay * 24 * 60 * 60 * 1000;
  today.setTime(day); //7 dias atras desde la fecha maxima
  start = `${today.getFullYear()}-${today.getMonth() + 1}-${today.getDate()}`;

  return { start, end };
}
